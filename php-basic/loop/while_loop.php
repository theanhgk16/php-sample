Vòng lặp trong khi PHP
Vòng whilelặp thực thi một khối mã miễn là điều kiện được chỉ định là đúng.

cú pháp:
while (condition is true) {
    code to be executed;
}

eg:
<?php
$x = 1;

while($x <= 5) {
    echo "The number is: $x <br>";
    $x++;
}
?>